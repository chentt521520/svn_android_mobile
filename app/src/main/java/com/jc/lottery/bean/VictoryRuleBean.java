public class VictoryRuleBean {
    private String rule_value;
    private String rule_name;
    private String rule_num;
    private String status;

    public String getRule_value() {
        return rule_value;
    }

    public void setRule_value(String rule_value) {
        this.rule_value = rule_value;
    }

    public String getRule_name() {
        return rule_name;
    }

    public void setRule_name(String rule_name) {
        this.rule_name = rule_name;
    }

    public String getRule_num() {
        return rule_num;
    }

    public void setRule_num(String rule_num) {
        this.rule_num = rule_num;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
